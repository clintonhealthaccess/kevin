package org.chai.kevin.imports;

/**
 * @author Jean Kahigiso M.
 *
 */
public class ImporterError {
	
	private String fileName;
	private Integer lineNumber;
	private String header;
	private String messageCode;

	public ImporterError(String fileName, Integer lineNumber, String header, String messageCode) {
		super();
		this.fileName = fileName;
		this.lineNumber = lineNumber;
		this.header = header;
		this.messageCode = messageCode;
	}
	
	public Integer getLineNumber() {
		return lineNumber;
	}
	
	public void setLineNumber(Integer lineNumber) {
		this.lineNumber = lineNumber;
	}
	
	public String getHeader() {
		return header;
	}
	
	public void setHeader(String header) {
		this.header = header;
	}
	
	public String getMessageCode() {
		return messageCode;
	}
	
	public void setMessageCode(String messageCode) {
		this.messageCode = messageCode;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result
				+ ((fileName == null) ? 0 : fileName.hashCode());
		result = prime * result + ((header == null) ? 0 : header.hashCode());
		result = prime * result
				+ ((lineNumber == null) ? 0 : lineNumber.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		ImporterError other = (ImporterError) obj;
		if (fileName == null) {
			if (other.fileName != null)
				return false;
		} else if (!fileName.equals(other.fileName))
			return false;
		if (header == null) {
			if (other.header != null)
				return false;
		} else if (!header.equals(other.header))
			return false;
		if (lineNumber == null) {
			if (other.lineNumber != null)
				return false;
		} else if (!lineNumber.equals(other.lineNumber))
			return false;
		return true;
	}

	@Override
	public String toString() {
		return "ImporterError [fileName=" + fileName + ", lineNumber="
				+ lineNumber + ", header=" + header + ", messageCode="
				+ messageCode + "]";
	}

}
